<template>
  <div
    class="top-0 h-full pointer-events-auto lg:sticky lg:left-0 min-h-fill-available lg:h-screen lg:top-header lg:w-1/5"
  >
    <div class="w-auto h-full d-bg-header dark:lg:bg-transparent lg:bg-transparent">
      <nav
        class="
          flex
          lg:flex-col
          justify-between
          lg:justify-start lg:max-w-sm
          w-full
          overflow-y-auto
          text-sm
          font-medium
          lg:h-[reset]
          h-(full-header)
          d-scrollbar
        "
      >
        <div class="py-4 pr-0">
          <ul class="flex flex-wrap lg:flex-col">
            <li v-for="category in categories" :key="category.name">
              <button
                class="flex justify-between w-full px-4 py-2 focus:outline-none focus:ring-transparent"
                :class="{
                  'rounded-md bg-gray-100 dark:bg-white dark:bg-opacity-10': category.name === selectedCategory
                }"
                @click="selectCategory(category.name)"
              >
                <span class="truncate max-w-48 font">{{ category.name }}</span>
                <span
                  v-if="category.name === selectedCategory"
                  class="rounded-xl bg-white dark:bg-sky-darkest ml-2 px-2 py-0.5 text-xs"
                >
                  {{ category.count }}
                </span>
              </button>
            </li>
          </ul>
        </div>
      </nav>
    </div>
  </div>
</template>
<script lang="ts">
import { defineComponent } from '@nuxtjs/composition-api'
import { useFuse } from '../../../plugins/fuse'

export default defineComponent({
  setup() {
    const fuseOptions = {
      threshold: 0.1,
      keys: []
    }

    const events = []
    const categories = []

    const { selectedCategory, selectCategory } = useFuse(events, fuseOptions)

    return {
      categories,
      selectedCategory,
      selectCategory
    }
  }
})
</script>
